def bubble_sort(arr): 
    for i in range(len(arr)-1):
        for j in range(len(arr)-1-i):
            if arr[j] > arr[j+1]:
                # swap two elements
                arr[j], arr[j+1] = arr[j+1], arr[j]
        print(i, arr)


if __name__ == "__main__":
    arr = [99, 44, 6, 2, 1, 5, 63, 87, 283, 4, 0]
    print(f"Before sorting: {arr}")

    bubble_sort(arr)
    print(f"After sorting: {arr}")

